@Composable
fun CatalogoKmpTheme( // Nome alterado para refletir o projeto KMP
    darkTheme: Boolean = isSystemInDarkTheme(),
    // Dynamic color é mais complexo em KMP e geralmente desabilitado
    // para consistência, a menos que haja uma lib KMP específica para isso.
    // dynamicColor: Boolean = false,
    content: @Composable () -> Unit
) {
    val colorScheme = if (darkTheme) DarkColorScheme else LightColorScheme

    // Para controle da barra de status e edge-to-edge em KMP,
    // você precisará de implementações expect/actual ou bibliotecas KMP
    // que abstraem isso (ex: moko-graphics ou equivalentes para system UI).
    // A lógica que estava na MainActivity do Android precisará ser adaptada.

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        shapes = Shapes,
        content = content
    )
}